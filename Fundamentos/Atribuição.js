//Alguns operadores de atribuição
// o Simbolo sempre vem antes
const a = 7 // o "=" é uma atribuição
let b = 3 

b += a //Atribuição Aditiva "+=" junto com a atribuição eu adiciono um valor
console.log(b)

b -= 4 //b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 //b = b /2
console.log(b)

//"%" módulo; ele vai pegar o resto da divisão e dar um resultado para par ou impar
b %= 2 //b = b % 2 
console.log(b)