// declaração de função

function soma (x, y) {
    return x + y
}

//Função de expressão 
const sub = function (x, y){
    return x - y
}

//função nomeada expressão
const mult = function mult(x, y){
    return x * y
}

console.log(soma(2, 3))
console.log(sub(2 , 3))
console.log(mult(2, 3))